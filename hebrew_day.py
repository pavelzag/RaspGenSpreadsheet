def get_hebrew_day(eng_day):
    if eng_day == 'Sunday':
        return 'א'
    elif eng_day == 'Monday':
        return 'ב'
    elif eng_day == 'Tuesday':
        return 'ג'
    elif eng_day == 'Wednesday':
        return 'ד'
    elif eng_day == 'Thursday':
        return 'ה'
    elif eng_day == 'Friday':
        return 'ו'
    else:
        return 'ש'